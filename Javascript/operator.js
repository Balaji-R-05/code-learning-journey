// Assignment
let a = 15;
let b = '10'
console.log(a, b);


// Arithmetic operator
console.log(a + b);
console.log(a - b);
console.log(a * b);
console.log(a / b);
console.log(a % b);
console.log(b ** a);

// Arithmetic Assignment
a += 5
b -= 10
console.log(a, b);

// Increment and decrement
a++;
b--;
console.log(a, b);


// Relational operator

console.log(a > b);
console.log(a < b);
console.log(a >= b);
console.log(a <= b);
console.log(a == b);
console.log(a != b);
console.log(a === b);
console.log(a !== b);


